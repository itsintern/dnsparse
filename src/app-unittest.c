/**
 * Traps SIGSEGV in order to print out a crash backtrace, so that we can
 * get additional information in the field about what happened.
 */
void
_crash_handler(int sig)
{
    void *array[10];
    size_t size;

    /* Print out globals of which test we were doing at the time
     * of the crash. */
    printf("%s:%d: section=%u index=%u count=%u\n", g_name?g_name:"", g_line_number, (unsigned)g_section, (unsigned)g_index, (unsigned)g_count);
    
    /* Get up to 10 items from the stack backtrace */
    size = backtrace(array, 10);

    /* print out all the stack frames to <stderr> */
    fprintf(stderr, "Error: signal %d:\n", sig);
    backtrace_symbols_fd(array, (int)size, 2);
    
    /* exit program with failure indication*/
    exit(1);
}
