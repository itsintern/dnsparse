static int _test_rr(int rtype, const char *rdata, size_t rdlength, const char *expected, int line_number)
{
    static const size_t offset = 45;
    unsigned char *packet = malloc(rdlength + 64);
    int err;

    /* copy over packet header */
    memcpy(packet, packet00, offset);
    
    /* set the rr type field */
    packet[offset - 10 + 0] = (unsigned char)(rtype >> 8);
    packet[offset - 10 + 1] = (unsigned char)(rtype & 0xFF);
    
    /* rdlength field */
    packet[offset - 2 + 0] = (unsigned char)(rdlength>>8);
    packet[offset - 2 + 1] = (unsigned char)(rdlength&0xFF);
    memcpy(packet + offset, rdata, rdlength);
    
    err = _test_string(0, packet, offset + rdlength, "www.example.com.", rtype, expected, line_number);
    free(packet);
    
    if (err)
        return 1;
    else
        return 0;
}

/**
  * Tests a single resource-record that has bad contents. Therefore, this function
  * is looking for the inverse: to succeed, the test must return an error.
 */
static int _test_error(int rtype, const char *rdata, size_t rdlength, int line_number)
{
    static const size_t offset = 45;
    unsigned char *packet = malloc(rdlength + 64);

    /* I sometimes mistype the RTYPE when writing the unit test, so this warning
     * is just to tell me this fact. In other words, it's a failure of the unit
     * test framekwork itself, not the code under test. */
    if (rtype == -1) {
        fprintf(stderr, "[-] %d: unknown rtype=%d\n", line_number, rtype);
        return 1;
    }
    
    /* copy over packet header */
    memcpy(packet, packet00, offset);
    
    /* set the rr type field */
    packet[offset - 10 + 0] = (unsigned char)(rtype >> 8);
    packet[offset - 10 + 1] = (unsigned char)(rtype & 0xFF);
    
    /* rdlength field */
    packet[offset - 2 + 0] = (unsigned char)(rdlength>>8);
    packet[offset - 2 + 1] = (unsigned char)(rdlength&0xFF);
    memcpy(packet + offset, rdata, rdlength);
    
    /* We expect this test to fail */
    if (_test_string(0, packet, offset + rdlength, 0, 0, 0, 0)) {
        return 0;
    } else {
        fprintf(stderr, "[-] %d: error test failed\n", line_number);
        return 1;
    }
}



