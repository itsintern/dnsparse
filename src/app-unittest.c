/**
 * Called by the '_evil_allocator()', this uses the operating system 'mmap()'
 * system call instead of 'malloc()' to allocator memory. We don't actually
 * 'free()' the pages when asked, but instead mark them as inaccessible
 * using 'mprotect()', so that any attempt to read-after-free will be detected
 * by the unittest crashing.
 */
static void *
_mmap_allocate(size_t size)
{
#if defined(PROT_READ)
#if defined(MAP_ANONYMOUS)
    return mmap(
        NULL, size, PROT_READ | PROT_WRITE, MAP_PRIVATE | MAP_ANONYMOUS, -1, 0);
#elif defined(MAP_ANON)
    return mmap(
        NULL, size, PROT_READ | PROT_WRITE, MAP_PRIVATE | MAP_ANON, -1, 0);
#else
    int fd;
    void *p;
    fd = open("/dev/zero", O_RDWR);
    if (fd < 0)
        return NULL;
    p = mmap(NULL, size, PROT_READ | PROT_WRITE, MAP_PRIVATE, fd, 0);
    close(fd);
    return p;
#endif
#else
    return malloc(size);
#endif
}

/**
 * Called by the '_evil_allocator()', this uses the 'mprotect()' system call to
 * market the pages as unreadable/unwritable, which will cause the
 * unitest to crash quickly if there is a bug.
 */
static void
_mmap_free(void *old_mem, size_t old_size)
{
    /* fill with garbage values to cause a crash */
    memset(old_mem, 0xa3, old_size);
    
    /* attempt to mark the pages from the operating system to
     * cause a crash */
#if defined(PROT_NONE)
    mprotect(old_mem, old_size, PROT_NONE);
#else
    free(old_mem);
#endif
}

/**
 * This is a replacement allocator for the `dns_parse()` function that allocates
 * memory from the operating system instead of the heap. Freed pages
 * Freed pages are marked at inaccessable using 'mprotect()' to make it
 * more likely that bugs will cause a crash instead of going undetected.
 */
static void *
_evil_allocator(void *old_mem, size_t new_size, void *arena)
{
    size_t old_size;
    void *new_mem = NULL;
    
    (void)arena;

    if (old_mem == NULL) {
        /* The caller wants to allocate new memory for the first
         * time, equivelent to a call to 'malloc()' instead of 'realloc() */
        old_size = 0;
    } else {
        /* The caller wants to resize an existing memory segment, probably
         * to increase its size */
        old_size = *(size_t*)old_mem;
    }

    if (new_size == 0) {
        /* The caller wants to free existing memory. */
        if (old_mem && old_size ) {
            _mmap_free(old_mem, old_size);
        } else {
            ; /* The caller wants to do nothing */
        }
        return NULL;
    }
    
    if (new_size) {
        /* For evil allocation, alway allocate completely new memory
         * and copy over the contents. Had this been a good allocator,
         * we'd do our best to expand an existing memory segment without
         * needing a copy */
        new_mem = _mmap_allocate(new_size);
        if (old_size)
            memcpy(new_mem, old_mem, old_size);
    } else {
        /* The caler wants to call 'free()' to free the memory */
        ;
    }
    
    /* Always free the old memory, if it exists. Evil allocation
     * means using 'mprotect()' to mark this as unreadable/unwritable
     * to detect bugs. */
    if (old_mem && old_size)
        _mmap_free(old_mem, old_size);
    
    return new_mem;
}



/**
 * Forces subsequent memory allocations to fail with an out-of-memory
 * condition.
 */
static void *
_oom_allocator(void *old_mem, size_t new_size, void *arena)
{
    size_t *count = (size_t *)arena;
    
    if (*count == 0) {
        /* If we've decremented the counter to zero, then simulate
         * out of memory */
        free(old_mem);
        return NULL;
    } else {
        /* Return a valid reallocation, and decrement the counter */
        (*count)--;
        return realloc(old_mem, new_size);
    }
}



enum {
    Found_Nothing,
    Found_Record,
    Found_Name,
    Found_Contents,
};
int
_process_record(struct dnsrrdata_t *rr, const char *expected_name, int expected_type, const char *expected_contents, int is_debug)
{
    char output[2048];
    int err = 0;
    int result = Found_Nothing;

    /* If not "IN" class, then skip this */
    if (rr->rclass != 1)
        return result;

    /* Convert the record to "presentation" format, so that we can do comparisons
    * on it */
    err = dns_format_rdata(rr, output, sizeof(output));
    if (err) {
       dns_format_rdata_generic(rr->unknown.buf, rr->unknown.length, output, sizeof(output));
    }
    result = Found_Record;

    /* Look for a record that matches the expected results */
    if (expected_name && strcmp((char*)rr->name, expected_name) == 0) {
        result = Found_Name;
        if (rr->rtype == expected_type && expected_contents != NULL) {
            if (strcmp(output, expected_contents) == 0) {
                result = Found_Contents;
           }
       }
    }

    /* If debugging, print presentation format to command line */
    if (is_debug && rr->rtype != 41) {
       printf("%s%-23s %u\tIN\t%-7s %s\n",
          (rr->section == 0) ? ";" : "",
           rr->name,
           rr->ttl,
           dns_name_from_rrtype(rr->rtype), output);
    }

    return result;
}

/**
 * Parses the DNS response, testing first to make sure it has no parsing errors,
 * and second that it contains the desired record.
 */
static int 
_test_string(int is_debug, const unsigned char *buf, size_t length, const char *expected_name, int expected_type, const char *expected_contents, int line_number)
{
    struct dns_t *dns = NULL;
    size_t i;
    int found = Found_Nothing;
    unsigned options = 0;
    
    /* We save these global variables in our test-harnass so that we can get
     * access back to them later when printing error messages */
    g_name = expected_name;
    g_line_number = line_number;

    /* Setup the custom allocator for use with this parse. We use an evil
     * allocator designed to expose bugs rather than just built-in heap */
    dns = dns_parse_allocator(_evil_allocator, 0, 0);
    if (dns == 0 || dns->error_code != 0) {
        dns_parse_free(dns);
        if (expected_name)
            fprintf(stderr, "[-] %d: memory failure: %s\n", line_number, expected_name);
                return 1;
    }
    
    /*
     * Parse the packet
     */
    dns = dns_parse(buf, length, options, dns);
    if (dns == 0 || dns->error_code != 0) {
        dns_parse_free(dns);
        if (expected_name)
            fprintf(stderr, "[-] %d: parse failure: %s\n", line_number, expected_name);
        return 1;
    }

    /* Decode answers */
    g_section = 1;
    g_count = dns->answer_count;
    for (i=0; i<dns->answer_count; i++) {
        int x;
        g_index = i;
        x = _process_record(&dns->answers[i], expected_name, expected_type, expected_contents, is_debug);
        if (found < x)
            found = x;
    }

    /* Decode name servers */
    g_section = 2;
    g_count = dns->nameserver_count;
    for (i=0; i<dns->nameserver_count; i++) {
        int x;
        g_index = i;
        x = _process_record(&dns->nameservers[i], expected_name, expected_type, expected_contents, is_debug);
        if (found < x)
            found = x;
    }

    /* Decode additional */
    g_section = 3;
    g_count = dns->additional_count;
    for (i=0; i<dns->additional_count; i++) {
        int x;
        g_index = i;
        x = _process_record(&dns->additional[i], expected_name, expected_type, expected_contents, is_debug);
        if (found < x)
            found = x;
    }

    /* Free the parsed DNS structure */
    dns_parse_free(dns);

    if (expected_name && found < Found_Name) {
        if (!is_debug) {
            _test_string(1, buf, length, expected_name, expected_type, expected_contents, line_number);
            fprintf(stderr, "[-] %d: not found: %s\n", line_number, expected_name);
        }
        return 1;
    }
    if (expected_contents && found < Found_Contents) {
        if (!is_debug) {
            _test_string(1, buf, length, expected_name, expected_type, expected_contents, line_number);
            fprintf(stderr, "[-] %d: not found: %s\n", line_number, expected_contents);
        }
        return 1;
    }
    
    return 0;
}

